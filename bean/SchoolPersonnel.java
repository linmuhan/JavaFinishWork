package bean;

public class SchoolPersonnel {
    private int id; //用户的ID
    private String name; //用户的姓名

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
